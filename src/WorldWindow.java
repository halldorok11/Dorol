
public class WorldWindow {
	public int x1; //bottom left x position of the worldwindow
	public int x2; //upper right x position of the worldwindow
	public int y1; //bottom left y position of the worldwindow
	public int y2; //upper right y position of the worldwindow
	
	public WorldWindow(int x1, int x2, int y1, int y2) {
		this.x1 = x1;
		this.x2 = x2;
		this.y1 = y1;
		this.y2 = y2;
	}
}